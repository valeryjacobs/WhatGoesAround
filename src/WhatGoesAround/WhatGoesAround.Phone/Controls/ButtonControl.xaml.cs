namespace WhatGoesAround.Phone.Controls
{
    public sealed partial class ButtonControl : UserControl
    {
        public ButtonControl()
        {
            this.InitializeComponent();
        }



        public int Radius
        {
            get { return (int)GetValue(RadiusProperty); }
            set { SetValue(RadiusProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Radius.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty RadiusProperty =
            DependencyProperty.Register("Radius", typeof(int), typeof(ButtonControl), new PropertyMetadata(20));



        public Color Color
        {
            get { return (Color)GetValue(ColorProperty); }
            set { SetValue(ColorProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Color.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty ColorProperty =
            DependencyProperty.Register("Color", typeof(Color), typeof(ButtonControl), new PropertyMetadata(Color.FromArgb(255, 0, 0, 0)));
        
    }


}
